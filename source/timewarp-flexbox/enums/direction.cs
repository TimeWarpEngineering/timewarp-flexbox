namespace TimeWarp.Flexbox;

/// <summary>
/// Represents the text/layout direction for RTL support.
/// </summary>
public enum Direction
{
  /// <summary>
  /// Inherit direction from parent node.
  /// </summary>
  Inherit,

  /// <summary>
  /// Left-to-right direction.
  /// </summary>
  Ltr,

  /// <summary>
  /// Right-to-left direction.
  /// </summary>
  Rtl
}
