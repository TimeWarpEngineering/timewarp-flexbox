namespace TimeWarp.Flexbox;

/// <summary>
/// Represents the layout axis for flexbox calculations.
/// </summary>
public enum Axis
{
  /// <summary>
  /// Horizontal axis (left to right or right to left).
  /// </summary>
  Row,

  /// <summary>
  /// Vertical axis (top to bottom or bottom to top).
  /// </summary>
  Column
}
